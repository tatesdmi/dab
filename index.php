<p class="txt">Naravno, nisam pristala na to, jer u onome što sam rekla nije bilo ni klevete ni neistine. Činjenica je da su ljudi naučili kako se nositi bez liječnika i osobnih trenera i da ne troše ogroman novac da bi postigli savršenu formu i da to nije moj problem. Zaista, postoji toliko mnogo prirodnih sredstava za održavanje vašeg tijela u izvrsnom stanju!</p>
